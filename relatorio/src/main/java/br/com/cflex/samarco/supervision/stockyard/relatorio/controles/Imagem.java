import java.awt.Image;



public class Imagem {
	
	private Image image;
	
	public Imagem(Image img) {
		this.image = img;
	}

	/**
	 * @return the image
	 */
	public Image getImage() {
		return image;
	}

	/**
	 * @param image the image to set
	 */
	public void setImage(Image image) {
		this.image = image;
	}
}
