package com.hdntec.gestao.exceptions;

public class SelecaoObjetoModoEmpilhamentoException extends Exception {

    public SelecaoObjetoModoEmpilhamentoException() {
        super();
    }

    public SelecaoObjetoModoEmpilhamentoException(String message) {
        super(message);
    }

    public SelecaoObjetoModoEmpilhamentoException(String message, Throwable cause) {
        super(message, cause);
    }

    public SelecaoObjetoModoEmpilhamentoException(Throwable cause) {
        super(cause);
    }
}
