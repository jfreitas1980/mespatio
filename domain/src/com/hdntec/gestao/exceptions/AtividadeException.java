package com.hdntec.gestao.exceptions;

public class AtividadeException extends Exception {

    public AtividadeException() {
        super();
    }

    public AtividadeException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public AtividadeException(String arg0) {
        super(arg0);
    }

    public AtividadeException(Throwable arg0) {
        super(arg0);
    }
}