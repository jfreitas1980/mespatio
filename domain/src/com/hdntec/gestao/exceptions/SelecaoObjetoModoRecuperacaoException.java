package com.hdntec.gestao.exceptions;

public class SelecaoObjetoModoRecuperacaoException extends Exception {

    public SelecaoObjetoModoRecuperacaoException() {
        super();
    }

    public SelecaoObjetoModoRecuperacaoException(String message) {
        super(message);
    }

    public SelecaoObjetoModoRecuperacaoException(String message, Throwable cause) {
        super(message, cause);
    }

    public SelecaoObjetoModoRecuperacaoException(Throwable cause) {
        super(cause);
    }
}
