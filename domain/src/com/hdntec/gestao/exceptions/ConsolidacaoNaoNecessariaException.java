package com.hdntec.gestao.exceptions;

public class ConsolidacaoNaoNecessariaException extends Exception {

    public ConsolidacaoNaoNecessariaException() {
        super();
    }

    public ConsolidacaoNaoNecessariaException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public ConsolidacaoNaoNecessariaException(String arg0) {
        super(arg0);
    }

    public ConsolidacaoNaoNecessariaException(Throwable arg0) {
        super(arg0);
    }
}
