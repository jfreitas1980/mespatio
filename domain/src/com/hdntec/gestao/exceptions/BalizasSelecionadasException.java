public class BalizasSelecionadasException extends Exception {

    private static final long serialVersionUID = -1625687606487259750L;

    /**
    *
    */
   public BalizasSelecionadasException()
   {
      super();
   }

   /**
    * @param arg0
    * @param arg1
    */
   public BalizasSelecionadasException(String arg0, Throwable arg1)
   {
      super(arg0, arg1);
   }

   /**
    * @param arg0
    */
   public BalizasSelecionadasException(String arg0)
   {
      super(arg0);
   }

   /**
    * @param arg0
    */
   public BalizasSelecionadasException(Throwable arg0)
   {
      super(arg0);
   }

}
