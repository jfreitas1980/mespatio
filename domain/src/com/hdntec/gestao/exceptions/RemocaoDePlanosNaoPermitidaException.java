package com.hdntec.gestao.exceptions;

public class RemocaoDePlanosNaoPermitidaException extends Exception {

    public RemocaoDePlanosNaoPermitidaException() {
        super();
    }

    public RemocaoDePlanosNaoPermitidaException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public RemocaoDePlanosNaoPermitidaException(String arg0) {
        super(arg0);
    }

    public RemocaoDePlanosNaoPermitidaException(Throwable arg0) {
        super(arg0);
    }
}
