package com.hdntec.gestao.exceptions;

public class BalizaInvalidaException extends Exception {

    public BalizaInvalidaException() {
        super();
    }

    public BalizaInvalidaException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public BalizaInvalidaException(String arg0) {
        super(arg0);
    }

    public BalizaInvalidaException(Throwable arg0) {
        super(arg0);
    }
}
