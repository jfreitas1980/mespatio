package com.hdntec.gestao.cliente.util.tabela;

import java.awt.Color;

public class CorLinhasTabela {
    
    /** numero da linha a ser colorida */
    private Integer numeroLinha;
    
    /** Cor da linha */
    private Color corLinha;

    public Color getCorLinha() {
        return corLinha;
    }

    public void setCorLinha(Color corLinha) {
        this.corLinha = corLinha;
    }

    public Integer getNumeroLinha() {
        return numeroLinha;
    }

    public void setNumeroLinha(Integer numeroLinha) {
        this.numeroLinha = numeroLinha;
    }
    
}
